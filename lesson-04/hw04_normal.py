# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

import random
import os
path = os.path.join('number.txt')
file_number = open(path, 'w', encoding='UTF-8')
for _ in range(2500):
    file_number.write(str(random.randint(0, 9)))
file_number.close()
file_number = open(path, 'r', encoding='UTF-8')
char = file_number.read(1)
max_number = [char, len(char)]
current_number = [char, len(char)]
print(max_number)
print(current_number)
while len(char) != 0:
    if current_number[0] == char:
        current_number[1] += 1
    else:
        if max_number[1] < current_number[1]:
            max_number = current_number
        current_number = [char, len(char)]
    char = file_number.read(1)
print(max_number)
print('Наибольшая полседовательность: ', max_number[0]*max_number[1])